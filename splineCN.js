let MutationObserver = window.MutationObserver || window.WebKitMutationObserver;
let MutationObserverConfig = {
  childList: true,
  subtree: true,
  attributeFilter: ['data-label'],
  characterData: true
};

let observer = new MutationObserver(function (mutations) {
  let treeWalker = document.createTreeWalker(
    document.body,
    NodeFilter.SHOW_ALL,
    {
      acceptNode: function (node) {
        if (node.nodeType === 3 || "placeholder" in node || "value" in node || "title" in node) {
          return NodeFilter.FILTER_ACCEPT;
        } else {
          return NodeFilter.FILTER_SKIP;
        }
      }
    },
    false
  );
  let dataMap = new Map();
  allData.forEach(([key, val]) => {
    if (key && !dataMap.has(key)) {
      dataMap.set(key, val);
    }
  });
  let currentNode = treeWalker.currentNode;

  while (currentNode) {
    if (currentNode.nodeType === 3) {
      let key1 = currentNode.textContent;
      if (dataMap.has(key1)) {
        currentNode.textContent = dataMap.get(key1)
        if (location.pathname.indexOf("community") != -1 || location.pathname.indexOf("@") != -1) {
          if (key1 === 'Follow') {
            currentNode.textContent = '关注'
          }
          if (key1 === 'Max') {
            currentNode.textContent = ' Max'
          }
        }
        if (location.pathname.indexOf("team-settings") != -1) {
          if (key1 === 'Viewer') {
            currentNode.textContent = '查看者'
          }
          if (key1 === 'Editor') {
            currentNode.textContent = '编辑者'
          }
        }
        //全匹配文本替换
      } else {
        for (i in Sdata) {
          if (Sdata[i][0] || Sdata[i][1]) {
            var tor_1 = Sdata[i][0].split('@Aa')[0];
            var tor_2 = Sdata[i][0].split('@Aa')[1];

            var tra_1 = Sdata[i][1].split('@Aa')[0];
            var tra_2 = Sdata[i][1].split('@Aa')[1];

            if (key1.includes(tor_1) && key1.includes(tor_2)) { currentNode.textContent = key1.replace(tor_1, tra_1).replace(tor_2, tra_2) }
          }
        }
      }
      //含有变量
    } else {
      let key2 = currentNode.getAttribute('title');
      let key4 = currentNode.getAttribute('placeholder');
      let key6 = currentNode.getAttribute('value');
      if (dataMap.has(key2)) currentNode.setAttribute('title', dataMap.get(key2));
      if (dataMap.has(key4)) currentNode.setAttribute('placeholder', dataMap.get(key4));
      if (!document.getElementsByClassName("input-signup-occupation")) {
        if (dataMap.has(key6)) currentNode.setAttribute('value', dataMap.get(key6));
      }
    }

    currentNode = treeWalker.nextNode();


    if (document.getElementsByClassName("main-menu-create")[0]) {
      document.getElementsByClassName("main-menu-create")[0].style.height = `420px`
    }
    if (document.getElementsByClassName(`SharingModals_inviteEmail__3tn8V`)[0]) {
      document.getElementsByClassName(`SharingModals_inviteEmail__3tn8V`)[0].children[0].style.width = `75%`
    }
    if (document.getElementsByClassName(`PaymentMethod_stripeWrapper__3rT62`)[0]) {
      document.getElementsByClassName(`PaymentMethod_stripeWrapper__3rT62`)[0].style.gridTemplateColumns = `3fr 1.2fr 1fr`
    }
  }
});

window.setTimeout(() => {
  observer.observe(document.body, MutationObserverConfig)
}, 0);
